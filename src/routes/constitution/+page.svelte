<h1 style="font-weight: 400;"><strong>The By-laws of the RedCom League</strong></h1>
<p style="font-weight: 400;"><strong>&nbsp;</strong></p>
<p style="font-weight: 400;"><strong>Number of teams</strong></p>
<p style="font-weight: 400;">12</p>
<p style="font-weight: 400;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="font-weight: 400;"><strong>Divisions </strong></p>
<p style="font-weight: 400;">3 (Random assignment)</p>
<p style="font-weight: 400;">After each season, the divisions will be recast randomly</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Fee:&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">50$ per Season/Team</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;">Seasons 2022 and 2023 (i.e. 100 $) to be paid upfront before Startup Draft</p>
<p style="font-weight: 400;">League fees will be managed through Leaguesafe</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;">Future Draft Picks can be traded only in case that Participation fee for that season has been paid in full.</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><span style="text-decoration: line-through; color: #ff0000;">Late payment for 2024 will cause Rookie Picks to be removed from the involved manager (more details will follow)</span></p>
<p style="font-weight: 400;">New rule applicable from 2023-03-25 onwards:</p>
<p style="font-weight: 400;">League fees must be paid 1 year in advance. Deadline: 1 day prior to the official NFL rookie draft</p>
<p style="font-weight: 400;">Example 1) in order to have no penalty rookie picks removed, league fee 2025 must be paid until approx. end April 2024</p>
<p style="font-weight: 400;">Example 2) in order to have no penalty rookie picks removed, league fee 2026 must be paid until approx. end April 2025</p>
<p style="font-weight: 400;">and so on&hellip;</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;">In case of delayed payments, the following penalty will be applied</p>
<p style="font-weight: 400;">Up to 1 week delay = no 1<sup>st</sup> round draft pick in the corresponding draft year (i.e. 2025 in example 1, 2026 in example 2)</p>
<p style="font-weight: 400;">Up to 2 weeks delay = no 1<sup>st</sup> + 2<sup>nd</sup> round draft pick</p>
<p style="font-weight: 400;">Up to 3 weeks delay = no 1<sup>st</sup> + 2<sup>nd</sup> + 3<sup>rd</sup> round draft pick</p>
<p style="font-weight: 400;">Up to 4 weeks delay = no 1<sup>st</sup> + 2<sup>nd</sup> + 3<sup>rd&nbsp; </sup>+&nbsp; 4<sup>th</sup> round draft pick</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Yearly Prizes:</strong></p>
<p style="font-weight: 400;">1st Place = 350 $</p>
<p style="font-weight: 400;">2nd Place = 175 $</p>
<p style="font-weight: 400;">3rd Place = 75 $</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;">Starting Lineup:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="font-weight: 400;"><strong>10 Roster Spots</strong></p>
<p style="font-weight: 400;">&nbsp;</p>
<ul>
<li>1 QB</li>
<li>2 RB</li>
<li>2 WR</li>
<li>1 TE</li>
<li>1 Super Flex</li>
<li>2 Flex (1 Slot added on 2023-04-26)</li>
<li>1 DEF</li>
</ul>
<p style="font-weight: 400;"><span style="text-decoration: line-through; color: #ff0000;">1 K (removed on 2023-04-23)</span></p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;">Super Flex = QB, RB, WR or TE</p>
<p style="font-weight: 400;">Flex = RB, WR or TE</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;">Each Starting Lineup slot will lock individually at the scheduled game time of the player in each slot.</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Positional Limits&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">A max. of 2 DEF can be held on Roster</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Startup Draft Positions:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">Drawn at random when league is paid in full</p>
<p style="font-weight: 400;">Startup Draft: 24 round serpentine (snake) online draft - Veterans &amp; Free agents only</p>
<p style="font-weight: 400;">Online Slow Draft with a 12-hour timer per pick</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Rookie Draft 2022:&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">Reversed order of Startup Draft / 4 Rounds in Snake format</p>
<p style="font-weight: 400;">Online Slow Draft with a 12-hour timer per pick</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Rookie Draft 2023 and onwards&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">Linear Drafts of 4 rounds, will include rookies only and will take place after the NFL draft</p>
<p style="font-weight: 400;">Online Slow Draft with an 8 hours timer per pick</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Draft Picks Assignment&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<ul>
<li>Will be assigned based on Max Points For (during Regular Season) for the 9 teams not winning any Prizes</li>
</ul>
<p style="font-weight: 400;">(i.e. Team with lowest Max PF will get 1st Pick of each Round, Team with 2nd lowest Max PF will get 2nd Pick, etc.)</p>
<ul>
<li>10th Pick of each Round will be assigned to 3rd Placed Team of previous Season</li>
<li>11th Pick of each Round will be assigned to 2nd Placed Team of previous Season</li>
<li>12th Pick of each Round will be assigned to 1st Placed Team of previous Season</li>
</ul>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Format (Regular Season):&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">Weeks 1-14</p>
<p style="font-weight: 400;">Head 2 Head Matchups (two games each against Divisional Teams / one game each against non Divisional Teams)</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Format (League Playoffs):&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">Six (6) teams advance to League Playoffs.&nbsp;</p>
<p style="font-weight: 400;">Three (3) Division winners&nbsp;- Seeds #1 - #3 based on best Record / Tie breaker is Points For</p>
<p style="font-weight: 400;">Two (2) teams with the best records among the remaining teams - Seeds #4 - #5 based on best Record / Tie breaker is Points For</p>
<p style="font-weight: 400;">One (1) team with the highest scored points for among the remaining teams - Seed #6</p>
<p style="font-weight: 400;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="font-weight: 400;"><strong>Weeks 15-17</strong></p>
<p style="font-weight: 400;">Top 2 seeds get bye week in Week 15</p>
<p style="font-weight: 400;">During Week 15, seed #3 plays against seed #6 / seed #4 plays against seed #5. The winners move on to the next round</p>
<p style="font-weight: 400;">During Weeks 16, seeds #1 will play against the lowest remaining seeded team and seed #2, will play against the highest remaining from Wild Card Round&nbsp;</p>
<p style="font-weight: 400;">During Week 17, there will be the RedCom Bowl and the final for 3rd Place</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Free Agency In Season:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">Waivers by way of blind bidding (FAAB)</p>
<p style="font-weight: 400;">Each team gets $100 blind bidding &ldquo;dollars&rdquo; for the entire Season</p>
<p style="font-weight: 400;">Continuous Waivers 12am EST</p>
<p style="font-weight: 400;">1 weekly free agency (FA) on Sunday</p>
<p style="font-weight: 400;">NFL players whose teams played on Thursday may be dropped from rosters on Friday as long as they were not in that team&rsquo;s Thursday&rsquo;s starting lineup</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>TIME PLAYERS ARE ON WAIVERS AFTER DROP&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">1 Day</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>GAMES WAIVERS CLEAR&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">Tuesday End of Day (00:00 PST)</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Free Agency Off Season:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">First free agency is scheduled one day after Rookie draft is completed</p>
<p style="font-weight: 400;">Each team gets $100 blind bidding &ldquo;dollars&rdquo; for the entire Off-season</p>
<p style="font-weight: 400;">Continuous Waivers 12am EST for the entire Off-Season</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>TIME TO REVIEW PENDING TRADES</strong></p>
<p style="font-weight: 400;">None - Trades are processed immediately</p>
<p style="font-weight: 400;">Any sign of collusion to maximize the chance of winning will be examined throughfully and in confirmed cases will cause the removal of the involved managers from the League.</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Trade Deadline&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">None</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;"><strong>Roster Requirement</strong>:</p>
<p style="font-weight: 400;">24 Total roster spots during NFL season</p>
<p style="font-weight: 400;">4 Taxi Squad slots</p>
<p style="font-weight: 400;">5 Injured Reserve (IR) slots / Eligible IR player status types are SUS (suspended) / COV (covid) &amp; IR (Injured reserve)</p>
<p style="font-weight: 400;">Players with (Q) Questionable,&nbsp; (D) Doubtful or (O) Out designation are not eligible for the injured reserve</p>
<p style="font-weight: 400;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
