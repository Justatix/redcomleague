<p style="font-weight: 400;"><strong>Tie Breaker&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="font-weight: 400;">In case of a tie during the playoffs the following tie-breaker rules will apply:</p>
<p style="font-weight: 400;">1) Bench Points Total (not including Taxi and IR)</p>
<p style="font-weight: 400;">2) QB Points</p>
<p style="font-weight: 400;">3) RB Points</p>
<p style="font-weight: 400;">4) WR Points</p>
<p style="font-weight: 400;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="font-weight: 400;"><strong>Rule changes&nbsp; </strong></p>
<p style="font-weight: 400;">All formal rule changes must be done during the offseason. The offseason period is considered the day after the RedCom Superbowl and the day before the rookie draft occurs.</p>
<p style="font-weight: 400;">&nbsp;</p>
<p style="font-weight: 400;">To amend any rule or regulation as defined in the bylaws, or to add another rule or regulation to it, a league member must bring the suggestion to the attention of the Commissioner in writing. The Commissioner will then present all of the specifics of the rules to the league members. A vote will be held and 75 % members of the league (9 of 12) must vote in favour <span style="text-decoration: line-through; color: #ff0000;">all members of the league must vote unanimously</span> to change an existing rule or add a new rule. If a suggestion is voted down, the owner who offered the suggestion can revise the rule offering and present again. If the suggestion is voted down a second time, the matter will be closed for that season and can be reopened the following offseason.</p>
